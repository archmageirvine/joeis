package irvine.oeis.a012;

import java.util.Arrays;

import irvine.math.group.PolynomialRingField;
import irvine.math.polynomial.Polynomial;
import irvine.math.q.Q;
import irvine.math.q.Rationals;
import irvine.math.z.Z;
import irvine.oeis.Sequence;

/**
 * A012085 Even coefficients in expansion of cos(x)/sqrt(cos(2*x)).
 * @author Sean A. Irvine
 */
public class A012085 implements Sequence {

  private static final PolynomialRingField<Q> RING = new PolynomialRingField<>(Rationals.SINGLETON);
  private static final Polynomial<Q> X2 = RING.create(Arrays.asList(Q.ZERO, Q.TWO));
  private int mN = -2;
  private Z mF = Z.ONE;

  @Override
  public Z next() {
    mN += 2;
    if (mN == 0) {
      return Z.ONE;
    }
    mF = mF.multiply(mN).multiply(mN - 1);
    return RING.coeff(RING.cos(RING.x(), mN), RING.sqrt(RING.cos(X2, mN), mN), mN).multiply(mF).toZ();
  }
}
