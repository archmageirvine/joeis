package irvine.oeis.a028;

import java.util.ArrayList;
import java.util.Collections;
import java.util.HashSet;

import irvine.math.LongUtils;
import irvine.math.z.Z;
import irvine.oeis.Sequence;

/**
 * A028492 Similarity classes of triangles which can be drawn using the lattice points in an n X n grid for vertices.
 * @author Sean A. Irvine
 */
public class A028492 implements Sequence {

  // Based loosely on some Python code by Anthony Wharton

  private int mN = 0;

  @Override
  public Z next() {
    ++mN;
    final HashSet<ArrayList<Long>> seenSides = new HashSet<>();
    // (x,y) = y * mN + x
    long cnt = 0;
    final int lim = mN * mN;
    for (int a = 0; a < lim; ++a) {
      final int ax = a % mN;
      final int ay = a / mN;
      for (int b = a + 1; b < lim; ++b) {
        final int bx = b % mN;
        final int by = b / mN;
        for (int c = b + 1; c < lim; ++c) {
          final int cx = c % mN;
          final int cy = c / mN;
          final long area2 = (bx - ax) * (cy - ay) - (cx - ax) * (by - ay);
          if (area2 == 0) {
            continue;
          }
          final long s0 = (bx - ax) * (bx - ax) + (by - ay) * (by - ay);
          final long s1 = (cx - ax) * (cx - ax) + (cy - ay) * (cy - ay);
          final long s2 = (bx - cx) * (bx - cx) + (by - cy) * (by - cy);
          final long gcd = LongUtils.gcd(s0, s1, s2);
          final ArrayList<Long> ss = new ArrayList<>(3);
          ss.add(s0 / gcd);
          ss.add(s1 / gcd);
          ss.add(s2 / gcd);
          Collections.sort(ss);
          if (seenSides.add(ss)) {
            ++cnt;
          }
        }
      }
    }
    return Z.valueOf(cnt);
  }
}
