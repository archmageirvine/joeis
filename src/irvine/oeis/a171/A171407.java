package irvine.oeis.a171;

import irvine.math.z.Z;
import irvine.oeis.Sequence1;

/**
 * A171407 This is a sequence demonstrating a congruence property of second order exponential functions. Let f(n) = a^(b^n) + c where a,b,c &amp; n belong to N, a, b &amp; c fixed. Then f(n + k*phi(phi(f(n)) is congruent to 0 (mod(f(n)). Here k belongs to N. This is a sequence of quotients generated by f(n + k*f(n))/f(n) when a &amp; b = 2, c= 3 and n = 1. when a &amp; b = 2 and c =3 when n = 1.
 * @author Sean A. Irvine
 */
public class A171407 extends Sequence1 {

  private int mN = 0;

  @Override
  public Z next() {
    return Z.ONE.shiftLeft(1L << (1 + 2 * ++mN)).add(3).divide(7);
  }
}
